CREATE TABLE IF NOT EXISTS companies (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(160) NOT NULL,
    slug VARCHAR(120) NOT NULL UNIQUE,
    status VARCHAR(30) NOT NULL DEFAULT 'ACTIVE'
);

CREATE TABLE IF NOT EXISTS roles (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(60) NOT NULL UNIQUE,
    description VARCHAR(240) NOT NULL
);

CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id BIGINT NOT NULL REFERENCES companies(id),
    username VARCHAR(80) NOT NULL,
    name VARCHAR(160) NOT NULL,
    email VARCHAR(160) NOT NULL,
    password VARCHAR(255) NOT NULL,
    role_id BIGINT NOT NULL REFERENCES roles(id),
    status VARCHAR(30) NOT NULL DEFAULT 'ACTIVE',
    CONSTRAINT uk_users_company_username UNIQUE (company_id, username),
    CONSTRAINT uk_users_company_email UNIQUE (company_id, email)
);

CREATE TABLE IF NOT EXISTS vehicles (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id BIGINT NOT NULL REFERENCES companies(id),
    code VARCHAR(80) NOT NULL,
    plate VARCHAR(80) NOT NULL,
    label VARCHAR(180) NOT NULL,
    status VARCHAR(40) NOT NULL,
    driver VARCHAR(160),
    device_id VARCHAR(120) NOT NULL,
    model VARCHAR(120),
    year INTEGER NOT NULL,
    unit_type VARCHAR(100),
    load_capacity_kg INTEGER NOT NULL,
    latitude DOUBLE PRECISION,
    longitude DOUBLE PRECISION,
    current_temperature VARCHAR(60),
    temperature_state VARCHAR(80),
    door_state VARCHAR(80),
    cooling_unit_state VARCHAR(80),
    last_communication VARCHAR(120),
    CONSTRAINT uk_vehicles_company_code UNIQUE (company_id, code),
    CONSTRAINT uk_vehicles_company_plate UNIQUE (company_id, plate),
    CONSTRAINT uk_vehicles_company_device UNIQUE (company_id, device_id)
);

CREATE TABLE IF NOT EXISTS sensor_parameters (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id BIGINT NOT NULL REFERENCES companies(id),
    vehicle_id BIGINT NOT NULL REFERENCES vehicles(id),
    code VARCHAR(100) NOT NULL,
    type VARCHAR(100) NOT NULL,
    unit VARCHAR(60),
    last_value VARCHAR(120) NOT NULL DEFAULT 'Sin lectura',
    status VARCHAR(30) NOT NULL DEFAULT 'ACTIVE',
    CONSTRAINT uk_sensor_parameters_company_code UNIQUE (company_id, code)
);

CREATE TABLE IF NOT EXISTS alerts (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id BIGINT NOT NULL REFERENCES companies(id),
    type VARCHAR(80) NOT NULL,
    severity VARCHAR(40) NOT NULL,
    title VARCHAR(180) NOT NULL,
    description TEXT NOT NULL,
    vehicle_label VARCHAR(180) NOT NULL,
    vehicle_code VARCHAR(80) NOT NULL,
    occurred_at_label VARCHAR(80) NOT NULL,
    status VARCHAR(40) NOT NULL,
    duration VARCHAR(80) NOT NULL,
    reading VARCHAR(120),
    icon VARCHAR(120)
);

CREATE INDEX IF NOT EXISTS idx_users_company_id ON users(company_id);
CREATE INDEX IF NOT EXISTS idx_vehicles_company_id ON vehicles(company_id);
CREATE INDEX IF NOT EXISTS idx_sensor_parameters_company_id ON sensor_parameters(company_id);
CREATE INDEX IF NOT EXISTS idx_sensor_parameters_vehicle_id ON sensor_parameters(vehicle_id);
CREATE INDEX IF NOT EXISTS idx_alerts_company_id ON alerts(company_id);
CREATE INDEX IF NOT EXISTS idx_alerts_company_vehicle_type ON alerts(company_id, vehicle_code, type);

